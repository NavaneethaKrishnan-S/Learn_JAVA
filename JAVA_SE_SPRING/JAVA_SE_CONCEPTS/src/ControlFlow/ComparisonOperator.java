
package ControlFlow;

public class ComparisonOperator {
    public static void main(String[] args) {
        /*
        1. Greater than (>)
        2. Greater than or equal to (>=)
        3. Lesser than (<)
        4. Lesser than or equal to (<=)
        5. Equal to (==)
        6. Not Equal to (!=)        
        */
        double yourAge = 21.12;
        double fatherAge = 21.13;
        
        System.out.println(yourAge < fatherAge);
    }
}
